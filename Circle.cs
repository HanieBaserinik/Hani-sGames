namespace GameCollection
{
    public class Circle
    {
        public int X { get; set; }
        public int Y { get; set; }
        
        public Circle()
        {
            X = 0;
            Y = 0;
        }
        
        public Circle(int x, int y)
        {
            X = x;
            Y = y;
        }
    }
}
